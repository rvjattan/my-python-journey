# Some additional list methods:

# .count() - to count the number of occurrences of an element in a list

# .insert() - to insert an element into to a specific index of a list

# .pop() - to remove an element from a specific index or from the end of the list

# range() - a built in python function to create a sequence of integers

# len() - a built in python function to get length of the list

# .sort() / .sorted() - built in function to sort a list


