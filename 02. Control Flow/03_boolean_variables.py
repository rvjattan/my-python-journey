# A variable that is assigned a True or False value is called a Boolean Variable

# example

set_to_true = True
set_to_false = False


# We can also set a variable equal to a boolean expression

bool_one = 5 != 7 
print(bool_one)    # True

bool_two = 1 + 1 != 2
print(bool_two)    # False

bool_three = 3 * 3 == 9
print(bool_three)  # True
